package es.iesjandula.alejandro_cazalla_perez.transparencia14;

public class ACPVariablesJava01 {

	public static void main(String[] args) {
		int x = 144, y = 999, suma, resta, multiplicacion, division;
		String nombre = "Alejandro Cazalla Perez";
		
		suma = x+y;
		System.out.println(x + " + " + y + " = " + suma);
		
		resta = x-y;
		System.out.println(x + " - " + y + " = " + resta);
		
		multiplicacion = x*y;
		System.out.println(x + " * " + y + " = " + multiplicacion);
		
		division = x/y;
		System.out.println(x + " / " + y + " = " + division);
		
		System.out.println(nombre);
	}

}
